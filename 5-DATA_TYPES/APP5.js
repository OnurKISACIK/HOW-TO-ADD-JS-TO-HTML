//* String = WRITTEN TEXTS ARE CALLED STRING AND
//* ALSO IF WE USE IT WITHIN DOUBLE QUOTATION MARK TYPE IS ALSO CHANGING AS STRING

//*-------------------------------------------------- */
//*EXAMPLE 1
let names = "David";
console.log(names);
console.log(typeof names);

//?OUTCOME: DAVID
//?OUTCOME: STRING

//*-------------------------------------------------- */
//*EXAMPLE 2

let cars = "5";
console.log(cars);
console.log(typeof names);

//?OUTCOME: 5
//?OUTCOME: STRING

//! IF WE USE DOUBLE QUOTATION MARKS AND INSERTING INSIDE SOME NUMBERS TYPE WILL BE CONSIDERED AS STRING

//*-------------------------------------------------- */

//*Number = IF IT IS NOT WITHIN DOUBLE QUOTATION MARKS IT IS NUMBER

//*EXAMPLE

let cards = 14;
console.log(cards);
console.log(typeof cards);

//?OUTCOME: 14
//?OUTCOME: NUMBER

