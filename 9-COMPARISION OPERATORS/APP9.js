//**---------------- COMPARISION OPERATORS -------------------- */

//* !   = WHEN WE USE ALWAYS CHANGE THE RESULTS OPPOSITE
//* ==  = EQUAL
//* >   = BIGGER
//* <   = SMALLER
//* >=  = BIGGER AND EQUAL
//* <=  = SMALLER AND EQUAL

//*--------------------------------------------- */
console.log(5 == 3);

//? OUTCOME: FALSE

console.log(5 !== 3);

//? OUTCOME: TRUE

//! "!" SYMBOL IS ALWAYS CHANGING THE RESULT AS OPPOSITE

//*--------------------------------------------- */

console.log(5 > 7);

//? OUTCOME: FALSE

console.log(!(5 > 7));

//? OUTCOME: TRUE

//*--------------------------------------------- */

console.log(6 < 8);

//? OUTCOME: TRUE

//*--------------------------------------------- */
let age = 25;
console.log(age >= 22);

//? OUTCOME:TRUE
//*--------------------------------------------- */

let hight = 150;
console.log(hight <= 120);

//? OUTCOME: FALSE

//*--------------------------------------------- */
